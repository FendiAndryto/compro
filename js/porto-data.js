const categoryMapping = {
  "digital-marketing": "Digital Marketing",
  "web-development": "Web Development",
  branding: "Branding",
  advertising: "Advertising",
};

function loadPortfolioItems() {
  return portoData;
}

function getProjectsByCategory(categoryType) {
  return Object.values(portoData).filter(
    (project) => project.categoryType === categoryType
  );
}

function loadPortfolioSections() {
  const categories = [
    "digital-marketing",
    "web-development",
    "branding",
    "advertising",
  ];
  let sectionsHTML = "";

  categories.forEach((categoryType) => {
    const projects = getProjectsByCategory(categoryType);
    if (projects.length > 0) {
      sectionsHTML += generateCategorySection(categoryType, projects);
    }
  });

  return sectionsHTML;
}

function generateCategorySection(categoryType, projects) {
  const categoryTitle = categoryMapping[categoryType].toUpperCase();

  let sectionHTML = `
    <section class="container">
      <div class="member__tittle text-center mt-5">
        <h1>${categoryTitle}</h1>
      </div>
  `;

  for (let i = 0; i < projects.length; i += 3) {
    sectionHTML += '<div class="row mt-5 gap-4">';

    for (let j = i; j < Math.min(i + 3, projects.length); j++) {
      const project = projects[j];
      const carouselId = `carousel${categoryType.replace("-", "")}${
        project.id
      }`;

      sectionHTML += `
        <div class="col-md portfolio-card">
          <div id="${carouselId}" class="carousel slide" data-bs-ride="carousel">
            <div class="carousel-inner">
      `;

      project.images.forEach((image, index) => {
        const activeClass = index === 0 ? "active" : "";
        sectionHTML += `
          <div class="carousel-item ${activeClass}">
            <img src="${image}" class="d-block w-100" alt="..." onclick="openModal(this.src, '${carouselId}')">
          </div>
        `;
      });

      sectionHTML += `
            </div>
            <a class="carousel-control-prev" href="#${carouselId}" role="button" data-bs-slide="prev">
              <span class="carousel-control-prev-icon" aria-hidden="true"></span>
            </a>
            <a class="carousel-control-next" href="#${carouselId}" role="button" data-bs-slide="next">
              <span class="carousel-control-next-icon" aria-hidden="true"></span>
            </a>
          </div>
          <div class="card-info mt-2 porto-clickable" data-porto-id="${project.id}" style="cursor: pointer;">
            <h5>${project.title}</h5>
            <p>${project.description}</p>
          </div>
        </div>
      `;
    }

    sectionHTML += "</div>";
  }

  sectionHTML += "</section>";
  return sectionHTML;
}
